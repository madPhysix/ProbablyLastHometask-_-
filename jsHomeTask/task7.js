let a = 90
let b = 18
let operation = '/'

switch(operation)
{
    case '+':
        console.log(a+b)
        break
    case '-':
        console.log(a-b)
        break
    case '*':
        console.log(a*b)
        break
    case '/':
        console.log(a/b)
        break
    default:
        console.log("Invalid Input")
        break
}